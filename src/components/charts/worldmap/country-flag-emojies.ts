// Function to convert Unicode string like "U+1F1FF U+1F1FC" to actual emoji
const convertUnicodeToEmoji = (unicodeString: string): string => {
  if (!unicodeString) return ""

  // Split by spaces and convert each Unicode code point
  const codePoints = unicodeString.split(" ")
  let emoji = ""

  for (const codePoint of codePoints) {
    if (codePoint.startsWith("U+")) {
      // Remove "U+" prefix and convert hex to decimal
      const hex = codePoint.substring(2)
      const decimal = parseInt(hex, 16)
      emoji += String.fromCodePoint(decimal)
    }
  }

  return emoji
}

export const getFlagEmojiByCountryName = (countryName: string) => {
  const countryData = flagMap[countryName.toLowerCase() as keyof typeof flagMap]
  if (!countryData) return ""

  // Convert Unicode string to actual emoji
  return convertUnicodeToEmoji(countryData.unicode)
}
